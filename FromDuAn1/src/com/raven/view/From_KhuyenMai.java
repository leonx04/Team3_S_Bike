    void loadData() {
        tbModel.setRowCount(0);
        listKhuyenMai = repo.getAllKM();
        for (KhuyenMai km : listKhuyenMai) {
            tbModel.addRow(new Object[]{
                km.getID(),
                km.getMaKM(),
                km.getGiaTriPhanTram(),
                km.getGiaTriTienMat(),
                km.getTrangthai(),
                km.getNgayBatDau(),
                km.getNgayKetThuc()
            });
        }
    }

