        ThuongHieu_repository repoBX = new ThuongHieu_repository();
        if (repoBX.InsertTH(TH) > 0) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            Fill(repoBX.getAllTH());
            this.clear();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
