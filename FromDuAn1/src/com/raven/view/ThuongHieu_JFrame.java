    void Fill(List<Model_Thuonghieu> list) {
        model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        sortSTTColumn();
        for (Model_Thuonghieu s : list) {
            model.addRow(s.toData());
        }
    }

    void show(int index) {
        String ma = tbl.getValueAt(index, 1).toString().trim();
        String ten = tbl.getValueAt(index, 2).toString().trim();
        txtMa.setText(ma);
        txtTen.setText(ten);

    }

    Model_Thuonghieu read() {
        Model_Thuonghieu th = new Model_Thuonghieu();
        th.setMa(txtMa.getText());
        th.setTenThuongHieu(txtTen.getText());
        return th;
    }

    void clear() {
        txtMa.setText(null);
        txtTen.setText(null);
    }

    public void sortSTTColumn() {
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(tbl.getModel());
        tbl.setRowSorter(sorter);

        ArrayList<RowSorter.SortKey> sortKeys = new ArrayList<>();
        sortKeys.add(new RowSorter.SortKey(0, SortOrder.ASCENDING));

        sorter.setSortKeys(sortKeys);
        sorter.sort();
    }

